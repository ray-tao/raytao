$("#line1").on("click", function () {
    $("#line1").addClass("div-animate");
    $("#wings1").addClass("img-animate");
    $("#line1>h3").addClass("img-animate");
    $("#line1").css("cursor", "default");

    setTimeout(function () {
        $("#line1").removeClass("div-animate");
        $("#wings1").removeClass("img-animate");
        $("#line1>h3").removeClass("img-animate");
        $("#line1").css("cursor", "pointer");
    }, 15000);
});

